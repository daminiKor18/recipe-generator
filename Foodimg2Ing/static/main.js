// --- 4. Artisan Voice Guide Logic ---
let synth = window.speechSynthesis;
let isCooking = false;
let currentStepIndex = 0;

function toggleVoiceGuide() {
    const voiceBtn = document.getElementById('voice-btn');
    const steps = document.querySelectorAll('.recipe-step-item');

    if (!isCooking) {
        isCooking = true;
        currentStepIndex = 0;
        voiceBtn.innerHTML = '<i class="fas fa-stop-circle mr-2"></i>Stop Guide';
        voiceBtn.classList.add('btn-speaking');
        showArtisanToast("Ingredi-Gen Voice Guide active.");
        readStep(currentStepIndex, steps);
    } else {
        stopVoiceGuide();
        showArtisanToast("Voice Guide paused.");
    }
}

function stopVoiceGuide() {
    isCooking = false;
    synth.cancel();
    const voiceBtn = document.getElementById('voice-btn');
    if (voiceBtn) {
        voiceBtn.innerHTML = '<i class="fas fa-microphone-alt mr-2"></i>Start Voice Guide';
        voiceBtn.classList.remove('btn-speaking');
    }
    document.querySelectorAll('.recipe-step-item').forEach(s => s.classList.remove('voice-highlight'));
}

function readStep(index, steps) {
    if (!isCooking || index >= steps.length) {
        if (isCooking) stopVoiceGuide();
        return;
    }

    steps.forEach((step, i) => {
        if (i === index) {
            step.classList.add('voice-highlight');
            step.scrollIntoView({ behavior: 'smooth', block: 'center' });
        } else {
            step.classList.remove('voice-highlight');
        }
    });

    const stepText = steps[index].querySelector('.step-text').innerText;
    const utterance = new SpeechSynthesisUtterance(`Step ${index + 1}. ${stepText}`);
    utterance.rate = 0.95;
    utterance.onend = () => {
        if (isCooking) {
            currentStepIndex++;
            setTimeout(() => readStep(currentStepIndex, steps), 1500);
        }
    };
    synth.speak(utterance);
}

// --- 5. FIXED: MULTI-PAGE PDF GENERATION (INGREDI-GEN BRANDED) ---
async function generatePDF() {
    if (!window.jspdf || !window.html2canvas) return;
    const { jsPDF } = window.jspdf;
    const element = document.getElementById("info"); 
    const btn = document.getElementById("pdf-btn");

    showArtisanToast("Plating your Ingredi-Gen PDF...");
    if (btn) btn.innerHTML = '<i class="fas fa-spinner fa-spin mr-1"></i> Plating...';
    
    try {
        const canvas = await html2canvas(element, {
            scale: 2,
            useCORS: true,
            backgroundColor: "#ffffff",
            height: element.scrollHeight,
            windowHeight: element.scrollHeight,
            onclone: (clonedDoc) => {
                // 1. Reveal hidden elements
                const hiddenElements = clonedDoc.querySelectorAll('.recipe-step-item, .nutrition-card, .stage-card, .philosophy-banner');
                hiddenElements.forEach(el => {
                    el.style.opacity = '1';
                    el.style.transform = 'none';
                    el.style.visibility = 'visible';
                });

                // 2. CRITICAL: Prevent "Badge Smearing" by locking dimensions
                const badges = clonedDoc.querySelectorAll('.step-badge');
                badges.forEach(badge => {
                    badge.style.display = "flex";
                    badge.style.width = "38px";
                    badge.style.height = "38px";
                    badge.style.minWidth = "38px";
                    badge.style.maxWidth = "38px";
                    badge.style.borderRadius = "50%";
                    badge.style.flexShrink = "0";
                    badge.style.backgroundColor = "#f8a5c2"; 
                });
                
                // 3. Flatten flex containers
                const steps = clonedDoc.querySelectorAll('.recipe-step-item');
                steps.forEach(step => {
                    step.style.display = "flex";
                    step.style.alignItems = "flex-start";
                });
            }
        });

        const imgData = canvas.toDataURL("image/png");
        const pdf = new jsPDF("p", "mm", "a4");
        const pageWidth = pdf.internal.pageSize.getWidth();
        const imgHeight = (canvas.height * pageWidth) / canvas.width;

        const finalPdf = new jsPDF("p", "mm", [pageWidth, imgHeight]);
        finalPdf.addImage(imgData, "PNG", 0, 0, pageWidth, imgHeight);
        finalPdf.save("IngrediGen-Artisan-Export.pdf");
        
        showArtisanToast("Ingredi-Gen export successful!");
    } catch (err) {
        console.error(err);
        showArtisanToast("Export failed.", "error");
    } finally {
        if (btn) btn.innerHTML = '<i class="fas fa-file-pdf mr-1"></i> Save PDF';
    }
}

// --- 6. Initialization & Scroll Reveal ---
document.addEventListener('DOMContentLoaded', () => {
    // Greeting
    const greetingText = document.querySelector('.display-4');
    if (greetingText) {
        const hour = new Date().getHours();
        let msg = (hour < 12) ? "Bonjour" : (hour < 17) ? "Good Afternoon" : "Good Evening";
        greetingText.innerHTML = `${msg}, <span class="text-gradient">Artisan Chef</span>`;
    }

    // Scroll Reveal
    const revealObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, { threshold: 0.1 });

    const animElements = document.querySelectorAll(
        '.recipe-step-item, .nutrition-card, .stage-card, .philosophy-banner, .animate-fade-in'
    );
    
    animElements.forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(25px)';
        el.style.transition = 'all 0.8s cubic-bezier(0.175, 0.885, 0.32, 1.275)';
        revealObserver.observe(el);
    });

    // Metadata Sync
    const rTitle = document.querySelector('.recipe-title')?.innerText;
    if (rTitle) document.title = `${rTitle} | Ingredi-Gen AI`;
    
    // Listeners
    const vBtn = document.getElementById('voice-btn');
    if (vBtn) vBtn.addEventListener('click', toggleVoiceGuide);
    initSocialSharing();

    window.addEventListener('scroll', () => {
        const nav = document.querySelector('.navbar');
        if (nav) window.scrollY > 50 ? nav.classList.add('scrolled') : nav.classList.remove('scrolled');
    });
});

