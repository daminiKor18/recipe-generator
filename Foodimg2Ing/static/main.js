// --- 1. PROFESSIONAL UI: TOAST SYSTEM ---
function showArtisanToast(message, type = "success") {
    const existingToast = document.querySelector('.artisan-toast');
    if (existingToast) existingToast.remove();

    const toast = document.createElement('div');
    toast.className = `artisan-toast animate-fade-in ${type === 'error' ? 'toast-error' : ''}`;
    const icon = type === 'success' ? 'fa-check-circle' : 'fa-exclamation-circle';
    
    toast.innerHTML = `<i class="fas ${icon} mr-2"></i> ${message}`;
    document.body.appendChild(toast);

    setTimeout(() => {
        toast.style.opacity = '0';
        toast.style.transform = 'translateY(-20px)';
        setTimeout(() => toast.remove(), 500);
    }, 3500);
}

// --- 2. Loading Text Rotation ---
function startLoadingSequence() {
    const statusMessages = [
        "Initializing Ingredi-Gen Engine...",
        "Scanning visual textures...",
        "Consulting culinary database...",
        "Structuring neural cooking steps...",
        "Plating your generated recipe..."
    ];
    const loadingText = document.querySelector('#loading h4');
    let messageIndex = 0;

    $("#info").fadeOut(400, function() {
        $("#loading").fadeIn(400).css("display", "flex");
        const messageInterval = setInterval(() => {
            if ($("#loading").is(":hidden")) {
                clearInterval(messageInterval);
                return;
            }
            $(loadingText).fadeOut(400, function() {
                messageIndex = (messageIndex + 1) % statusMessages.length;
                $(this).text(statusMessages[messageIndex]).fadeIn(400);
            });
        }, 3200);
    });
}

